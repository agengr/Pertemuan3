public class dOperatorTambahKurang {
    public static void main(String[] args) {
        //Program Penambahan
        int x, y, z;
        x = 42;
        y = x++;
        //tampilkan x, y saat ini
        z = ++x;
        //tampilkan x, z saat ini

        //Program Pengurangan
        int a, b, c;
        a = 42;
        b = a--;
        //tampilkan a, b saat ini
        c = --a;
        //tampilkan a, b saat ini
    }
}
