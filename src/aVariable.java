public class aVariable {

    //Deklarasi Tipe Data dan Variable
    static boolean b;
    static char c;
    static byte bt;
    static short s;
    static int i;
    static long l;
    static float f;
    static double d;

    public static void main(String args[]){

        //Cetak pada Layar
        System.out.println("Nilai Default untuk variable b tipe data boolean: " + b);
        System.out.println("Nilai Default untuk variable c tipe data character: " + c);
        System.out.println("Nilai Default untuk variable bt tipe data byte: " + bt);
        System.out.println("Nilai Default untuk variable s tipe data short: " + s);
        System.out.println("Nilai Default untuk variable i tipe data integer: " + i);
        System.out.println("Nilai Default untuk variable l tipe data long: " + l);
        System.out.println("Nilai Default untuk variable f tipe data float: " + f);
        System.out.println("Nilai Default untuk variable d tipe data double: " + d);
       }

}

